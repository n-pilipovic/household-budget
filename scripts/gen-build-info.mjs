#!/usr/bin/env node
/**
 * Generates src/app/build-info.ts before every build / dev-serve.
 *
 * Output exports the local build date (YYYY-MM-DD) and an optional
 * short commit SHA. Read by the Settings page so the "About" section
 * always reflects the deploy that's actually running, without manual
 * version bumps.
 *
 * The output file is .gitignored — never commit it.
 */
import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import path from 'node:path';

function pad(n) {
  return String(n).padStart(2, '0');
}

function localDate() {
  const d = new Date();
  return `${d.getFullYear()}-${pad(d.getMonth() + 1)}-${pad(d.getDate())}`;
}

function shortCommit() {
  // Cloudflare Pages exposes this during the build.
  if (process.env.CF_PAGES_COMMIT_SHA) {
    return process.env.CF_PAGES_COMMIT_SHA.slice(0, 7);
  }
  // Local fallback: ask git directly.
  try {
    return execSync('git rev-parse --short HEAD', {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
  } catch {
    return '';
  }
}

const date = localDate();
const commit = shortCommit();

const content = `// AUTO-GENERATED by scripts/gen-build-info.mjs at build time.
// Do not edit by hand or commit — this file is .gitignored and is
// regenerated on every \`pnpm build\` / \`pnpm start\`.

export const BUILD_DATE = '${date}';
export const BUILD_COMMIT = '${commit}';
`;

writeFileSync(path.resolve('src/app/build-info.ts'), content);
console.log(`✓ build-info: ${date}${commit ? ` · ${commit}` : ''}`);
